namespace Extend
{
    /// <summary>
    ///     Enumeration of all posible string compare options.
    /// </summary>
    public enum StringCompareOption
    {
        /// <summary>
        ///     Check if string is quals.
        /// </summary>
        Equals = 0,

        /// <summary>
        ///     Check if string starts with.
        /// </summary>
        StartsWith = 1,

        /// <summary>
        ///     Check if string ends with.
        /// </summary>
        EndsWith = 2,

        /// <summary>
        ///     Check if string contains.
        /// </summary>
        Contains = 3
    }
}