namespace Extend
{
    /// <summary>
    ///     Enumeration of all possible compare modes.
    /// </summary>
    public enum CompareMode
    {
        /// <summary>
        ///     Check if equals to value.
        /// </summary>
        Is = 0,

        /// <summary>
        ///     Check if is not equals to value.
        /// </summary>
        IsNot = 1
    }
}